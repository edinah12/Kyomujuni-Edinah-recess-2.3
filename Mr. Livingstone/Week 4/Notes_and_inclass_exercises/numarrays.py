#one-demensional array

import numpy as np
arr1 = np.array([1, 2, 3, 4, 5])
print(arr1)

#two dimensional array from list
arr2 = np.array([[1, 2, 3], [4, 5, 6]])
print(arr2)

print(arr1[0]) #accessing the first element of the array

print(arr2[0])
print(arr2[1][2]) #accessing the third element of the second row