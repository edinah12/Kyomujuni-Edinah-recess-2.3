#scikit library in python
#it's an open source python library that provides simple and efficient tools for data mining and data analysis. It is built on NumPy, SciPy, and matplotlib.
#it provides tools for data analysis and predictive modeling

#key features of scikit-learn:
#1- data preprocessing - this involves data splitting, feature scaling, feature selection, feature extraction
# data splitting-training and testing data sets
#feature scaling- normalizing or standardizing the range of independent variables or features
 #feature engineering-this involves selecting important features from our data to improve model performance
#also creating new features from existing ones to improve model performance

#Absolute Maximum Scaling - is a feature scaling method where each value is divided by the maximum absolute value of that feature
#model evaluation- this involves evaluating the performance of our model using metrics such as accuracy, precision, recall, and F1 score

#3- pipeline support - this allows us to create a sequence of data processing steps that can be applied to our data in a consistent manner

#4-support for integration with other libraries
#5-easy to use

